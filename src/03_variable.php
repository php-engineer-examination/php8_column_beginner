<?php

// 変数に文字を代入します
$string_value = 'string';
// 変数に入れた文字を出力します
print $string_value;
// (見た目上の理由で)改行を出力します
print "\n"; // ダブルクォートの中にバックスラッシュとnを続けて書くと、改行が出力されます(この行をコメントアウトして実行してみるのもよいでしょう)

// 変数に数値(整数)を代入します
$int_value = 1234;
// 変数に入れた数値を出力します
print $int_value;
// (見た目上の理由で)改行を出力します
print "\n";

// 変数に数値(小数)を代入します
$float_value = 1.23;
// 変数に入れた数値を出力します
print $float_value;
// (見た目上の理由で)改行を出力します
print "\n";

// 変数名の、大文字と小文字は区別されます
$string_value = 'lower string';
$STRING_value = 'upper string';
// 変数に入れた文字を出力します
print $STRING_value;
print "\n";


